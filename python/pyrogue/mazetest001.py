# most simple dungeon

# navigate the player (@) via w,a,s,d and pressing enter each turn

levels = []

l1 = "################################################\n"
l1+= "#..............................................#\n"
l1+= "#..............................................#\n"
l1+= "#..............................................#\n"
l1+= "#..............................................#\n"
l1+= "#..............................................#\n"
l1+= "#......................................#########\n"
l1+= "#......................................#.......#\n"
l1+= "#......................................#.......#\n"
l1+= "################.......................#.......#\n"
l1+= "#..............#.......................######..#\n"
l1+= "#..............#...............................#\n"
l1+= "#..............#...............................#\n"
l1+= "#..............#...............................#\n"
l1+= "#..............#...............................#\n"
l1+= "#..............#...............................#\n"
l1+= "#..............#...............................#\n"
l1+= "#..............................................#\n"
l1+= "################################################\n"


levels.append(l1)
level = levels[0]

playing = True
playerpos=[1,1] # x,y position of player

while playing:
    y = 0
    for line in level.split():
        x = 0
        for char in line:
            #print( line, char)
            #break
            if y == playerpos[1] and x == playerpos[0]:
                print("@",end="")
            else:
                print(char,end="")
            x +=1
        print() # new line
        y += 1
    i = input("your command: >>>").lower()
    if i in ["quit","exit", "q"]:
        playing = False
    elif i == "w":
        playerpos[1]-=1
    elif i == "s":
        playerpos[1]+=1
    elif i == "a":
        playerpos[0]-=1
    elif i == "d":
        playerpos[0]+=1
print("game over")
            
